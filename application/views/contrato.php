		<h3 align="center">REPÚBLICA BOLIVARIANA DE VENEZUELA</h3>
		<h3 align="center">COMISIÓN CENTRAL DE PLANIFICACIÓN</h3>
		<h3 align="center">SERVICIO NACIONAL DE CONTRATACIONES</h3>

		<p align="right">Caracas, 08 de septiembre de 2014</p>
		<p align="center">204° y 155°</p>

		<h3 align="center">PROVIDENCIA N° DG/2014/ A-0094</h3>

		<p class="justify">
			Quien suscribe, <b>ANTHONI CAMILO TORRES</b>, titular de la Cédula de Identidad <b>Nº 14.585.056</b>, en mi carácter de Director General
			del Servicio Nacional de Contrataciones, según consta en la Resolución CCP/DGCJ/Nº 001/2014 de fecha 07 de enero de 2014, publicada en la
			Gaceta Oficial de la República Bolivariana de Venezuela Nº 40.334 de fecha 15 de enero de 2014, en ejercicio de las atribuciones conferidas
			en los numerales 3 y 7 del artículo 22 de la Ley de Contrataciones Públicas, así como los numerales 1, 2, 6 y 17 del artículo 56 del Reglamento
			de la mencionada Ley, en concordancia con lo establecido en los artículos 10 y 11 del Decreto N° 6.217, con Rango, Valor y Fuerza de Ley Orgánica
			de la Administración Pública.
		</p>

		<h3 align="center">DECIDE:</h3>

		<p class="justify">
			<b>Artículo 1.</b> Se ordena, a través de la presente Providencia, la publicación en la Gaceta Oficial de la República Bolivariana de Venezuela
			del contenido íntegro de los <b>TÉRMINOS Y CONDICIONES GENERALES DEL SERVICIO "SISTEMA RNC EN LÍNEA"</b>, que se reproduce a continuación:
		</p>

		<h3 align="center">TÉRMINOS Y CONDICIONES GENERALES DEL SERVICIO "SISTEMA RNC EN LÍNEA"</h3>

		<p class="justify art">
			El uso del servicio denominado “SISTEMA RNC EN LÍNEA” que más adelante se describe ofrecido por el Servicio Nacional de Contrataciones,
			a cualquier persona natural o jurídica, en adelante y para todos los efectos de este documento denominado el USUARIO se regirá por los términos,
			modalidades y condiciones establecidas en las siguientes cláusulas:
		</p>

		<p class="justify art">
			<b>PRIMERA: DEFINICIONES:</b> A los fines de este documento se establecen las siguientes definiciones, algunas de las cuales podrán ser presentadas
			indistintamente en singular o plural según corresponda en relación con el texto en el cual aparezcan:
		</p>

		<p class="justify art">
			<b>SNC:</b> SERVICIO NACIONAL DE CONTRATACIONES.
		</p>

		<p class="justify art">
			<b>SERVICIO “SISTEMA RNC EN LÍNEA”:</b> Servicio descrito en la cláusula segunda de este documento.
		</p>

		<p class="justify art">
			<b>USUARIO:</b> Toda persona natural o jurídica que disfrute del servicio “SISTEMA RNC EN LÍNEA”.
		</p>

		<p class="justify art">
			<b>SEGUNDA:</b> El SNC ha implementado un sistema informático de telecomunicación denominado “SISTEMA RNC EN LÍNEA”, mediante el cual el USUARIO
			podrá, por intermedio de la Red Mundial de Información denominada Internet (World Wide Web), que permite la captura, procesamiento y consolidación
			de los datos de las personas interesadas en inscribirse en el Registro Nacional de Contratistas (RNC), facilitando los procesos de contratación
			mediante la inscripción, evaluación y suministro de la información necesaria para la escogencia de los participantes.
		</p>

		<p class="justify art">
			El Registro Nacional de Contratistas contiene la información de la calificación legal, financiera y la clasificación por especialidad de las
			Empresas, que desean contratar con el Estado Venezolano, de acuerdo a lo establecido en la Ley de Contrataciones Públicas, la cual es necesaria
			para la calificación general de las empresas por parte de los entes contratantes.
		</p>

		<p class="justify art">
			En todo caso, el SNC se reserva el derecho de modificar, ampliar o restringir el servicio “SISTEMA RNC EN LÍNEA”, pudiendo modificar o suprimir
			cualquiera de las funciones del mencionado sistema o incorporar otras a su libre elección, sin necesidad de notificación alguna al USUARIO y sin
			que ello conlleve ningún tipo de responsabilidad para el SNC. Las opciones específicas dentro de cada una de las funciones del sistema serán
			determinadas unilateralmente por el SNC, apareciendo identificadas en el propio sistema, pudiendo igualmente el SNC, en cualquier momento,
			modificar o suprimir cualquiera de dichas opciones, así como incorporar otras, sin necesidad de notificación alguna al USUARIO y sin que ello
			conlleve ningún tipo de responsabilidad para el SNC. El SNC determinará igualmente el horario durante el cual podrá ser utilizado el sistema
			“SISTEMA RNC EN LÍNEA”, sea en términos generales o en relación a cada una de las opciones en particular.
		</p>

		<p class="justify art">
			<b>TERCERA:</b> El USUARIO podrá acceder al uso del sistema “SISTEMA RNC EN LÍNEA” en la página de Internet www.snc.gob.ve. El USUARIO también
			podrá acceder al sistema “SISTEMA RNC EN LÍNEA”, a través de iconos de acceso directo ubicados en las diferentes páginas Web de personas naturales
			o jurídicas, públicas o privadas con los cuales el SNC tuviere suscrito el contrato de afiliación correspondiente, a fin de que el USUARIO pueda
			realizar la carga de los datos de su empresa, con la finalidad de ser incluido en el RNC, y a los Organismos del Estado obtener la información
			necesaria de los Contratistas inscritos en el RNC de manera confiable y oportuna, a través de Internet y cualesquiera otras operaciones en línea
			permitidas por el propio sistema e incorporadas por el SNC.
		</p>

		<p class="justify art">
			<b>CUARTA:</b> Los equipos, sistemas, programas de navegación por Internet y cualesquiera otros programas requeridos para poder disfrutar del servicio
			“SISTEMA RNC EN LÍNEA”, serán adquiridos o utilizados por el USUARIO a su costo y riesgo, y deberán tener las características técnicas necesarias
			para permitir el acceso a Internet. El SNC no será responsable en ningún caso por el buen funcionamiento, idoneidad, capacidad y compatibilidad de
			los equipos, sistemas y programas adquiridos o utilizados por el USUARIO a los efectos de disfrutar del servicio “SISTEMA RNC EN LÍNEA”.
		</p>

		<p class="justify art">
			<b>QUINTA:</b> El USUARIO accederá al uso del sistema “SISTEMA RNC EN LÍNEA” a través de la introducción de un correo electrónico válido y una
			contraseña, ya que este se convertirá posteriormente en el login de ingreso al Sistema, según lo solicite el propio sistema. Es entendido que para
			el acceso a determinados niveles o tipos de funciones, operaciones o transacciones, el sistema podrá requerir la introducción de claves de acceso
			complementarias, por el riesgo que las mismas conllevan, pudiendo asimismo el SNC requerir el cumplimiento de determinados requisitos y condiciones
			por parte del USUARIO. Las claves de acceso tendrán carácter personal, confidencial e intransferible, el SNC no tendrá conocimiento alguno de las
			mismas, y su finalidad es garantizar el exclusivo acceso del USUARIO y las personas por él autorizadas al sistema “SISTEMA RNC EN LÍNEA”, siendo
			responsabilidad exclusiva del USUARIO la guarda, cuido y custodia de sus claves de acceso. El USUARIO asumirá en forma exclusiva cualquier
			responsabilidad derivada o que pudiera derivarse directa o indirectamente de cambios en cualquiera de sus claves de acceso. En virtud de lo establecido
			en la presente cláusula el SNC no asumirá responsabilidad alguna por cualquier circunstancia derivada, directa o indirectamente, del conocimiento y
			eventual uso de cualesquiera de las claves de acceso del USUARIO por parte de terceros, por cualquier motivo que ello ocurriere, asumiendo el
			USUARIO cualquier responsabilidad al respecto. El USUARIO asumirá igualmente, cualesquiera riesgos y responsabilidades derivadas de permitir a
			cualesquiera personas la utilización o manipulación de los equipos y sistemas a través de los cuales se acceda al sistema “SISTEMA RNC EN LÍNEA”.
			Como consecuencia de todo lo antes expresado, todas las consultas y operaciones realizadas y las instrucciones, solicitudes y comunicaciones enviadas
			a través del sistema “SISTEMA RNC EN LÍNEA”, debidamente accedido por medio del uso de las correspondientes claves de acceso del USUARIO, serán
			consideradas como emanadas del mismo.
		</p>

		<p class="justify art">
			<b>SEXTA:</b> El SNC podrá limitar, restringir o suprimir el acceso del USUARIO a determinadas funciones u opciones en forma unilateral, a su solo
			criterio.
		</p>

		<p class="justify art">
			<b>SÉPTIMA:</b> El USUARIO se obliga a cumplir estrictamente en el uso del sistema “SISTEMA RNC EN LÍNEA” con todas las instrucciones contenidas en
			el mismo, asumiendo toda la responsabilidad por cualquier circunstancia derivada del uso o manejo incorrecto o inadecuado de dicho sistema.
		</p>

		<p class="justify art">
			<b>OCTAVA:</b> Es entendido que el servicio “SISTEMA RNC EN LÍNEA” tiene como única finalidad facilitar y agilizar la comunicación, a los fines
			especificados en este documento, entre el USUARIO y el SNC, rigiéndose cada una de las operaciones que pueden efectuarse y las instrucciones,
			solicitudes y comunicaciones en general que pueden ser enviadas a través del sistema “SISTEMA RNC EN LÍNEA”, por las normas y condiciones específicas
			establecidas para cada una en particular, en los respectivas Condiciones Generales o cualesquiera otras regulaciones aplicables a cada una de ellas;
			si no los hubiere, cada transacción se regirá por las condiciones y requisitos indicadas por el propio Sistema que se considerarán aceptadas al
			momento que el USUARIO realice la operación a través de “SISTEMA RNC EN LÍNEA”.
		</p>

		<p class="justify art">
			<b>NOVENA:</b> El SNC queda exento de cualquier responsabilidad por el no procesamiento de cualesquiera operaciones o consultas realizadas o
			instrucciones, solicitudes y comunicaciones de cualquier género y clase enviadas a través del sistema “SISTEMA RNC EN LÍNEA” si no se realizasen
			o enviasen de conformidad con lo establecido en las instrucciones contenidas en el propio sistema, en el presente documento, en las normas y
			procedimientos internos del SNC aplicables al servicio objeto de este documento, en la normativa referida en la cláusula precedente y en cualesquiera
			otras regulaciones que fueren aplicables.
		</p>

		<p class="justify art">
			<b>DÉCIMA:</b> El SNC se reserva el derecho de suspender temporalmente, en cualquier momento y sin necesidad de notificación alguna al USUARIO,
			la prestación del servicio “SISTEMA RNC EN LÍNEA”, en caso de ser necesario por razones de orden técnico, como mantenimiento del sistema, o por
			cualquier otro motivo que a juicio del SNC haga necesaria o conveniente dicha suspensión temporal.
		</p>

		<p class="justify art">
			<b>DÉCIMA PRIMERA:</b> El SNC no se hace responsable por la interrupción imprevista del servicio “SISTEMA RNC EN LÍNEA”, por dificultades en su
			funcionamiento, o por retardo en el tiempo de procesamiento de cualesquiera operaciones o consultas realizadas o instrucciones, solicitudes y
			comunicaciones de cualquier género causadas por fallas técnicas, fallas en el servicio eléctrico, en el servicio telefónico o de cualquier otra
			naturaleza, congestionamiento en la Internet, o por cualesquiera otras causas ajenas a la voluntad del SNC.
		</p>

		<p class="justify art">
			<b>DÉCIMA SEGUNDA:</b> Las tarifas que deberá pagar el USUARIO al SNC como remuneración por el uso del servicio “SISTEMA RNC EN LÍNEA”, o por cada
			una de las operaciones que se realicen a través del Sistema, así como la forma y oportunidad del pago y las demás obligaciones conexas y derivadas
			de las anteriores, serán establecidas unilateralmente por el SNC, pudiendo ser modificadas de la misma manera cada vez que el SNC lo considere
			conveniente, y podrán ser conocidas por el USUARIO a través de información disponible en la Sede del SNC, Registros Auxiliares, o en el propio
			sistema por cualquier otro medio que el SNC determinase a tales fines. El USUARIO se obliga a pagar al SNC la remuneración que éste estableciere
			según lo antes expresado. Las tarifas que el Servicio cobrará a los usuarios por el uso del sistema del Registro Nacional de Contratistas, se ajustará
			a lo señalado en la Providencia dictada para tal fin.
		</p>

		<p class="justify art">
			<b>DÉCIMA TERCERA:</b> Las consultas y operaciones efectuadas y las instrucciones, solicitudes, notificaciones y comunicaciones en general enviadas
			a través del sistema “SISTEMA RNC EN LÍNEA”, serán procesadas y ejecutadas según la naturaleza de cada una de ellas, en los plazos que estableciere el
			SNC a tales fines y que se informarán a través del propio sistema para cada una de las opciones y las mismas se considerarán aceptadas al momento que el
			USUARIO realice la operación a través del sistema y dispondrá de una cartelera de mensajes la cual solamente contendrá mensajes no leídos ya que al momento
			de leerlos son borrados automáticamente y se entenderá por notificado.
		</p>

		<p class="justify art">
			<b>DÉCIMA CUARTA:</b> La transmisión de datos por medio del servicio “SISTEMA RNC EN LÍNEA” estará debidamente codificada mediante la tecnología
			de encriptación, para suministrar al sistema la mayor seguridad posible, así como a las consultas, operaciones y transacciones que por su
			intermedio se realizasen y a las solicitudes e instrucciones que se enviasen.
		</p>

		<p class="justify art">
			<b>DÉCIMA QUINTA:</b> El SNC se reserva el derecho de modificar unilateralmente cualquier término o condición establecida en el presente documento,
			sin necesidad de aviso previo al USUARIO. Toda modificación que el SNC realice de acuerdo a las facultades aquí referidas será hecha del conocimiento
			del USUARIO, sea a través del propio sistema o por cualquier otro medio que el SNC determine a tal fin.
		</p>

		<p class="justify art">
			<b>DÉCIMA SEXTA:</b> El SNC, sin perjuicio para el USUARIO, podrá desincorporar del servicio “SISTEMA RNC EN LÍNEA”, a aquellos CONTRATISTAS que
			por la aplicación de la Ley de Contrataciones Públicas incurran en causales de suspensión del Registro Nacional de Contratistas, o cuando se venza
			el certificado de inscripción.
		</p>

		<p class="justify art">
			<b>DÉCIMA SEPTIMA:</b> El SNC se reserva el derecho de dejar de prestar el servicio “SISTEMA RNC EN LÍNEA” en cualquier momento, si así lo estimase
			conveniente, sin necesidad de justificación ni notificación alguna y sin que ello acarree responsabilidad alguna para el SNC. Así mismo el SNC se
			reserva el derecho de suspender en cualquier momento, en forma temporal o definitiva la prestación del servicio “SISTEMA RNC EN LÍNEA” a cualquier
			USUARIO sin necesidad de aviso ni justificación alguna. Una vez suspendido el servicio “SISTEMA RNC EN LÍNEA” indicará la opción para la inscripción y
			los certificados que estén vigentes se mantendrán en una base de datos disponible para todos los usuarios.
		</p>

		<p class="justify art">
			<b>DÉCIMA OCTAVA:</b> Queda entendido que la información suministrada por el USUARIO al Registro Nacional de Contratistas, es pública y podrá ser
			consultada por cualquier persona que la solicite, según lo establecido en la Ley de Contrataciones Públicas, publicada en la Gaceta Oficial de la
			República Bolivariana de Venezuela N° 39.503 del 06 de septiembre de 2010.
		</p>

		<p class="justify art">
			<b>DÉCIMA NOVENA:</b> Para todos los efectos derivados de este documento se elige como domicilio especial la ciudad de Caracas, a la Jurisdicción
			de cuyos Tribunales se someten las partes, sin perjuicio para el SNC de ocurrir a la jurisdicción de cualquier otro tribunal que resultare competente
			si así lo creyese conveniente y de conformidad con la Ley.
		</p>

		<p class="justify art">
			<b>VIGÉSIMA:</b> El USUARIO que modifique o altere el contenido de la data aprobada por el Registro Nacional de Contratistas, quedará sujeto a las
			sanciones civiles y penales que a tal fin estén contempladas en la Constitución de la República Bolivariana de Venezuela, las Leyes nacionales
			aplicables.
		</p>

		<p class="justify art">
			<b>VIGÉSIMA PRIMERA:</b> El USUARIO acepta y autoriza al Servicio Nacional de Contrataciones para que todas las comunicaciones, notificaciones de actos
			administrativos y demás informaciones emitidas por el Servicio Nacional de Contrataciones en el marco de la Ley de Contrataciones Públicas, su Reglamento,
			y cualquier otra normativa aplicable, le sean realizadas en forma exclusiva a través de la dirección de correo electrónico suministrada al momento
			de realizar su registro en el Sistema RNC en Línea, o en cualquier apartado, sección o ubicación pública de la página web del Servicio Nacional de
			Contrataciones o a la cual tenga acceso el USUARIO.
		</p>

		<p class="justify art">
			<b>VIGÉSIMA SEGUNDA:</b> Queda entendido y aceptado por el USUARIO, que la continuación y culminación del proceso de registro y posterior uso del
			Sistema RNC en Línea, implica la total aceptación de los términos y condiciones expuestos en este documento denominado "Contrato de Suscripción al
			Sistema RNC en Línea", así como de las demás normas que establezca el Servicio Nacional de Contrataciones. En ese sentido, y conforme a lo dispuesto
			en la cláusula anterior, se remitirá al USUARIO el presente "Contrato de Suscripción al Sistema RNC en Línea".
		</p>

		<p class="justify art">
			<b>VIGÉSIMA TERCERA:</b> Queda entendido y aceptado por el USUARIO, que en caso de constituir apoderado mediante documento debidamente autenticado,
			este asumirá en los mismos términos y condiciones las obligaciones y responsabilidades inherentes al USUARIO. Igualmente, el USUARIO será
			solidariamente responsable por  la actuación del apoderado, y tal solidaridad debe expresarse en el instrumento respectivo. En cualquier caso, y
			bajo cualquier circunstancia, el Servicio Nacional de Contrataciones o el Registro Nacional de Contratistas, podrá comunicarles indistintamente
			con EL USUARIO o su apoderado.
		</p>

		<p class="justify">
			<b>Artículo 2.</b> Se ordena la publicación del texto antes referido en la página web del Servicio Nacional de Contrataciones, www.snc.gob.ve.
		</p>

		<p class="justify">
			<b>Artículo 3.</b> La información y la documentación que debe acompañar los procesos de inscripción y actualización ante el Registro Nacional de
			Contratistas, deberán ser presentados en formato digital, y las instrucciones y manuales para la carga de data y consignación de la información y
			documentación digital ante el Servicio Nacional de Contrataciones, serán publicadas en la página web del órgano.
		</p>

		<p class="justify">
			<b>Artículo 4.</b> Las instrucciones o manuales a que se refiere el artículo anterior, establecerán los medios o soportes a ser utilizados para la
			consignación de la documentación en formato digital.
		</p>

		<p class="justify">
			<b>Artículo 5.</b> La implementación de lo dispuesto en el Artículo 3 deberá realizarse en forma progresiva, a fin de evitar discriminación en
			cuanto al acceso a los servicios que presta la institución y garantizándose en todo caso los derechos de las personas con difícil acceso a
			recursos tecnológicos. No obstante, cuando así lo considere conveniente el Servicio Nacional de Contrataciones fijará una fecha tope a partir
			de la cual se realizará la consignación de información o documentación exclusivamente en forma digital o electrónica.
		</p>

		<p class="justify">
			<b>Parágrafo Único:</b> Para la actualización de la información o documentación ante el Registro Nacional de Contratistas que se realice a partir
			del 01 de enero de 2015, los usuarios deberán presentar la información requerida en formato digital.
		</p>

		<p class="justify">
			<b>Artículo 6.</b> El Registro Nacional de Contratistas es el encargado de implementar los mecanismos de entrenamiento y capacitación tanto al
			personal como de los usuarios que así lo requieran en la correcta utilización al sistema RNC en línea, y la carga de la data para la consignación
			de la información digital, a los fines de dar cumplimiento a lo previsto en esta Providencia.
		</p>

		<p class="justify">
			<b>Artículo 7.</b> Se deroga la Providencia Administrativa  DG/2013/ A-0182 de fecha 04 de junio de 2013.
		</p>

		<p class="justify">
			<b>Artículo 8.</b> La presente Resolución entrará en vigencia a partir de su publicación en la Gaceta Oficial de la República Bolivariana de
			Venezuela.
		</p>

